                PreLoadMaps();
                initialized = true;
                Log("Initialized");
            }
            if (enabled && !EventSent && (!PhotonNetwork.IsMasterClient) && ((currentScene == "Map0") || (currentScene == "Map1")))
            {
                EventSent = true;
                string useCustom;
                if ((bool)CustomMultiplayerMaps.Settings[1].SavedValue)
                {
                    useCustom = "1";
                }
                else
                {
                    useCustom = "0";
